                            <h2>“Intel Inside“ là nhãn dán bạn sẽ thường thấy trên các máy tính hay laptop chạy Windows.
                                Nhưng vì sao trên Macbook lại không có dù&nbsp;máy tính Mac cũng sử dụng bộ xử lý Intel.
                                Apple có lí do gì của họ khi không dán bất kì nhãn nào của Intel lên các sản phẩm của
                                mình?</h2>
                            <h3><strong>1. Vì dán nhãn sẽ… không đẹp</strong></h3>
                            <p>Đó là những câu trả lời của hầu hết những người dùng Mac, nhưng vẫn chưa thuyết phục. Vậy
                                câu hỏi tại sao những hãng laptop khác lại đưa nhãn dán vào nơi dễ thấy nhất của máy
                                tính lại được đặt ra.</p>
                            <p>Cũng tương tự như việc máy tính của bạn bị cài một số phần mềm dùng thử không mong muốn,
                                lý do nhãn dán của Intel được xuất hiện chỉ có một và đó là “tiền”.</p>
                            <p>Intel sẽ trả tiền cho các nhà sản xuất để họ dán nhãn “Intel Inside” vào một góc dễ nhìn
                                của máy tính để người dùng biết rằng máy tính này có sử dụng con chip của Intel.</p>
                            <p>Riêng Apple vì đã là một trong những công ty giàu nhất thế giới nên họ sẵn sàng từ chối
                                một khoản tiền để chiếc Mac của mình trông “thoáng hơn”, hướng được sự chú ý của người
                                dùng đến các chi tiết khác của máy thay vì tập trung vào logo Intel.</p>
                            <h3><strong>2. Việc dán nhãn thật dư thừa</strong></h3>
                            <p>Vào năm 2006, Apple đã thông báo sẽ chuyển sang sử dụng chip của Intel cho các dòng máy
                                tính của mình. Sự thay đổi này đã khiến nhiều người tự hỏi: Liệu Apple có dán nhãn
                                “Intel Inside” vào máy tính Mac?</p>
                            <p>Khi được hỏi về điều này, Steve Jobs đã thẳng thừng nói “Không”. Ông chia sẻ thêm: “Chúng
                                tôi tự hào khi đưa sản phẩm của Intel vào máy tính Mac. Bằng việc kết hợp với hệ điều
                                hành của chúng tôi, chúng tôi đã khiến chúng hoạt động tốt với nhau và chúng tôi tự hào
                                về điều đó. Và mọi người đều biết rằng chúng tôi sử dụng chip của Intel.</p>
                            <p>Vì vậy việc đặt nhãn dãn của họ lên sản phẩm là thừa thãi. Chúng tôi thích nói về sản
                                phẩm bên trong chiếc hộp hơn và người dùng sẽ biết nó được dùng chip của Intel khi họ sử
                                dụng.”</p>
                            <h3><strong>3. Do Apple từng chế nhạo chip Intel</strong></h3>
                            <p>Dòng Mac chỉ mới sử dụng chip Intel vào năm 2006. Trước đó Apple sử dụng chip PowerPC như
                                một phần trong thỏa thuận với IBM và họ liên tục chứng minh rằng con chip này nhanh hơn
                                so với chip của Intel.</p>
                            <p>Thậm chí vào cuối thập niên 1990 Apple còn cho phát một đoạn video gây tranh cãi nhằm chế
                                nhạo chiến dịch quảng cáo của Intel với hình ảnh chip Intel được đặt trên lưng một con
                                ốc sên.</p>
                            <p>Theo Ken Segall, người đã từng hợp tác quảng cáo với Apple cho biết mục đích của họ làm
                                cho mọi người ám ảnh đến bức hình con chip Intel trên lưng chú ốc sên trong video. Chẳng
                                phải đem ra bàn luận về khả năng bộ vi xử lý PowerPC nhanh hơn Intel thông qua đoạn
                                video đó nữa.</p>
                            <p>“Tất nhiên, Intel không hề thích đoạn quảng cáo đó. Một trang web của Intel đã lên tiếng
                                và bác bỏ số liệu của Apple bằng những kết quả benchmark khác nhau. Lời đe dọa về một vụ
                                kiện đã được đưa ra. Steve Jobs đã nghĩ Intel sẽ mắc bẫy. Ông đã tưởng tượng về việc
                                hình ảnh chip Intel nằm trên lưng của con ốc sên sẽ được xuất hiện trên các tờ báo lớn
                                khắp thế giới” – Ken Segall cho biết.</p>
                            <p>Sau đó thì Táo khuyết đã phải bỏ chip PowerPC và chuyển sang dùng chip của Intel cho
                                Macbook. Có thể vì Apple không muốn bị cười nhạo rằng mình đi sử dụng con chip mà họ
                                từng chê bai nên chẳng có logo nào của Intel được dán lên cả.</p>
                            <h3><strong>4. Chiến lược quảng cáo xuất sắc của Intel</strong></h3>
                            <p>Vào đầu thập niên 1990, những cái tên của bộ vi xử lý dường như vô hình và không được
                                biết đến, cho đến khi chiến dịch quảng cáo của Intel đã thay đổi tất cả.</p>
                            <p>Với chiến lược quảng cáo hết sức thông minh, Intel đã thông báo đến người dùng về sức
                                mạnh tuyệt vời của bộ vi xử lý. Đồng thời Intel cũng đặc biệt lưu ý với mọi người nên
                                tìm mua những máy tính có dán nhãn “Intel Inside”. Và như đã đề cập, họ trả tiền cho các
                                nhà sản xuất laptop để giúp thương hiệu Intel phổ biến hơn qua các nhãn dán.</p>
                            <p>Nhờ vậy, nhiều người tiêu dùng hiện nay đều quen thuộc với thương hiệu Intel và giúp hãng
                                vươn lên trở thành nhà sản xuất chip hàng đầu thế giới trong 30 năm. Điều này có thể đã
                                làm Apple “ghen tị” và tìm cách không muốn quảng bá thêm cho Intel nữa.</p>


